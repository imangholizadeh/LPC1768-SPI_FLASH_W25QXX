unsigned int spi(unsigned int val)
{
	

  /* Write and Read a byte on SPI interface. */
	unsigned int received = 0;
	 FLASH_CS_LOW();
  LPC_SPI->SPDR = val;
  while( !(LPC_SPI->SPSR & 0x80));//wait for transfer to be completed
   received=LPC_SPI->SPDR;
	FLASH_CS_HIGH() ;
   return(received);
}


